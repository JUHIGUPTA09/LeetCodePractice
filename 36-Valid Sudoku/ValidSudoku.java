	public static void validSudoku(String[][] board)
	{
		Set<String> set=new HashSet<>();
		String row="";
		String col="";
		String box="";
		boolean flag=true;;
		for(int i=0;i<board.length;i++)
		{
			for(int j=0;j<board.length;j++)
			{
				if(board[i][j]!=".") {
					row="Row"+i+board[i][j];
					col="Col"+j+board[i][j];
					int boxDig=(i/3)*3 + (j/3);
					box="Box"+boxDig+board[i][j];
						if(set.contains(row) ||set.contains(col)||set.contains(box))
						{
						flag=false;
						break;
					    }
						else
						{
							set.add(row);
							set.add(col);
							set.add(box);
							
						}
				}
				
			}
			if(!flag)
			{
				break;
			}
		}
		
		System.out.println("The sudoku is valid:-"+flag);
	}
